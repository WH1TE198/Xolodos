import sqlite3
from contextlib import closing

DB_PATH = "app.db"


def _conn():
    return sqlite3.connect(DB_PATH)


def init_recipes_db():
    """Создаёт таблицы под рецепты/ингредиенты."""
    with _conn() as conn, closing(conn.cursor()) as c:
        c.execute("""
            CREATE TABLE IF NOT EXISTS recipes (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                title TEXT NOT NULL UNIQUE,
                steps TEXT,
                time_min INTEGER,
                difficulty TEXT
            )
        """)
        c.execute("""
            CREATE TABLE IF NOT EXISTS recipe_ingredients (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                recipe_id INTEGER NOT NULL,
                name TEXT NOT NULL,
                qty REAL,
                unit TEXT,
                FOREIGN KEY (recipe_id) REFERENCES recipes(id) ON DELETE CASCADE
            )
        """)
        conn.commit()


def add_recipe(title: str, steps: str, ingredients: list, time_min: int | None = None, difficulty: str | None = None) -> int:
    with _conn() as conn, closing(conn.cursor()) as c:
        c.execute(
            "INSERT INTO recipes(title, steps, time_min, difficulty) VALUES(?,?,?,?)",
            (title, steps, time_min, difficulty)
        )
        rid = c.lastrowid
        if ingredients:
            c.executemany(
                "INSERT INTO recipe_ingredients(recipe_id, name, qty, unit) VALUES(?,?,?,?)",
                [(rid, i.get("name"), i.get("qty"), i.get("unit")) for i in ingredients]
            )
        conn.commit()
        return int(rid)


def get_all_recipes() -> list[dict]:
    with _conn() as conn, closing(conn.cursor()) as c:
        rows = c.execute("SELECT id, title, steps, time_min, difficulty FROM recipes").fetchall()
        data = []
        for (rid, title, steps, tm, diff) in rows:
            ings = c.execute(
                "SELECT name, qty, unit FROM recipe_ingredients WHERE recipe_id=?",
                (rid,)
            ).fetchall()
            data.append({
                "id": rid,
                "title": title,
                "steps": steps or "",
                "time_min": tm,
                "difficulty": diff,
                "ingredients": [{"name": n, "qty": q, "unit": u} for (n, q, u) in ings],
            })
        return data


def get_recipe_by_id(recipe_id: int) -> dict | None:
    with _conn() as conn, closing(conn.cursor()) as c:
        row = c.execute(
            "SELECT id, title, steps, time_min, difficulty FROM recipes WHERE id=?",
            (recipe_id,),
        ).fetchone()
        if not row:
            return None

        rid, title, steps, tm, diff = row
        ings = c.execute(
            "SELECT name, qty, unit FROM recipe_ingredients WHERE recipe_id=?",
            (rid,),
        ).fetchall()
        return {
            "id": rid,
            "title": title,
            "steps": steps or "",
            "time_min": tm,
            "difficulty": diff,
            "ingredients": [{"name": n, "qty": q, "unit": u} for (n, q, u) in ings],
        }


def get_recipe_by_title(title: str) -> dict | None:
    with _conn() as conn, closing(conn.cursor()) as c:
        row = c.execute(
            "SELECT id FROM recipes WHERE title=?",
            (title,),
        ).fetchone()
        if not row:
            return None
        return get_recipe_by_id(row[0])

def upsert_recipe(title: str, steps: str, ingredients: list, time_min: int | None = None, difficulty: str | None = None) -> int:
    with _conn() as conn, closing(conn.cursor()) as c:
        row = c.execute("SELECT id FROM recipes WHERE title=? LIMIT 1", (title,)).fetchone()
        if row:
            rid = int(row[0])
            c.execute(
                "UPDATE recipes SET steps=?, time_min=?, difficulty=? WHERE id=?",
                (steps, time_min, difficulty, rid),
            )
            c.execute("DELETE FROM recipe_ingredients WHERE recipe_id=?", (rid,))
            if ingredients:
                c.executemany(
                    "INSERT INTO recipe_ingredients(recipe_id, name, qty, unit) VALUES(?,?,?,?)",
                    [(rid, i.get("name"), i.get("qty"), i.get("unit")) for i in ingredients],
                )
        else:
            c.execute(
                "INSERT INTO recipes(title, steps, time_min, difficulty) VALUES(?,?,?,?)",
                (title, steps, time_min, difficulty),
            )
            rid = int(c.lastrowid)
            if ingredients:
                c.executemany(
                    "INSERT INTO recipe_ingredients(recipe_id, name, qty, unit) VALUES(?,?,?,?)",
                    [(rid, i.get("name"), i.get("qty"), i.get("unit")) for i in ingredients],
                )

        conn.commit()
        return int(rid)

